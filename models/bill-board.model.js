class Advertisement {
  constructor(
    type_billboard,
    position,
    type_advertisement,
    size,
    amount,
    advertise_form,
    license
  ) {
    this.type_billboard = type_billboard;
    this.position = position;
    this.type_advertisement = type_advertisement;
    this.size = size;
    this.amount = amount;
    this.advertise_form = advertise_form;
    this.license = license;
  }
}
